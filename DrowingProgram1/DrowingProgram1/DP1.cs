using System;

namespace DrowingProgram1
{
    public class DP1
    {
        public void DrowALine(int x1, int x2, int y1, int y2)
        {
            Console.WriteLine($"Drowing program one drow a line: X1: {x1}, X2: {x2}, Y1: {y1}, Y2: {y2}");
